	local bin2hex = {
		["0000"] = "0",
		["0001"] = "1",
		["0010"] = "2",
		["0011"] = "3",
		["0100"] = "4",
		["0101"] = "5",
		["0110"] = "6",
		["0111"] = "7",
		["1000"] = "8",
		["1001"] = "9",
		["1010"] = "A",
	 ["1011"] = "B",
	 ["1100"] = "C",
	 ["1101"] = "D",
	 ["1110"] = "E",
	 ["1111"] = "F"
		}
	local function Bin2Hex(s)
	
		-- s 	-> binary string
		
		local l = 0
		local h = ""
		local b = ""
		local rem
		
		l = string.len(s)
		rem = l % 4
		l = l-1
		h = ""
		
			-- need to prepend zeros to eliminate mod 4
		if (rem > 0) then
			s = string.rep("0", 4 - rem)..s
		end
	
		for i = 1, l, 4 do
			b = string.sub(s, i, i+3)
			h = h..bin2hex[b]
		end
	
		return h
		
	end
	return Bin2Hex(Supplied)
